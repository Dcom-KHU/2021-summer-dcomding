import math

n = int(input())


# 1                                 -> 1
# 1 2                               -> 2
# 1 2 3                             -> 2
# 1 2 3 4         -> 2 4            -> 4
# 1 2 3 4 5       -> 2 4            -> 4
# 1 2 3 4 5 6     -> 2 4 6          -> 4
# 1 2 3 4 5 6 7   -> 2 4 6          -> 4
# 1 2 3 4 5 6 7 8 -> 2 4 6 8 -> 4 8 -> 8
# ...
# 1 2 3 ... n     -> ...            -> 2 ^ floor(log_2(n))
print(2 ** int(math.log2(n)))
